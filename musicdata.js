// Render Function
function renderMusic(data, id) {
    const grid = document.getElementById(id);
    grid.innerHTML = ""; // Clear existing content

    data.forEach((album) => {
        const li = document.createElement("li");
        li.innerHTML = `
            <img src="${album.img}" alt="${album.title}">
            <p class="atitle">${album.title}</p>
            <p class="adate">${album.date}</p>
            <div class="al-con">
                ${album.links
                    .map(
                        (link) =>
                            `<a href="${link.url}" title="${link.alt}" target="_blank">
                                <img src="${link.icon}" alt="${link.alt}">
                            </a>`
                    )
                    .join("")}
            </div>
	    <div class="tc">
          ${album.tags.map(tag => `<span class="tag">${tag}</span>`).join(" ")}
        </div>
        `;
        grid.appendChild(li);
    });
}

// Call the render function
renderMusic(musicSolo, "solo-grid");
renderMusic(musicGroup, "group-grid");
renderMusic(musicOthers, "others-grid");

function filterMusic(data, id, filterTag) {
    const filteredData = filterTag === "All" ? data : data.filter(album => album.tags.includes(filterTag));
    renderMusic(filteredData, id);
}

// Add Event Listeners to Filter Tags
document.querySelectorAll('.tc span').forEach(span => {
    span.addEventListener('click', (e) => {
        const filterTag = e.target.textContent;
        const parentTab = e.target.closest('.tab-content').id;

        if (parentTab === "solo") {
            filterMusic(musicSolo, "solo-grid", filterTag);
        } else if (parentTab === "group") {
            filterMusic(musicGroup, "group-grid", filterTag);
        } else if (parentTab === "others") {
            filterMusic(musicOthers, "others-grid", filterTag);
        }

        // Highlight active tag
        e.target.parentElement.querySelectorAll('span').forEach(s => s.classList.remove('active'));
        e.target.classList.add('active');
    });
});

